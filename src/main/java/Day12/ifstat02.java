package Day12;

public class ifstat02 {
    public static void main(String[] args) {

        String s = "yusuf";

        if (s.indexOf("y") == s.lastIndexOf("y")) {
            System.out.print("y");
        }

        if (s.indexOf("u") == s.lastIndexOf("u")) {
            System.out.print("u");
        }

        if (s.indexOf("s") == s.lastIndexOf("s")) {
            System.out.print("s");
        }

        if (s.indexOf("f") == s.lastIndexOf("f")) {
            System.out.print("f");
        }












    }
}
